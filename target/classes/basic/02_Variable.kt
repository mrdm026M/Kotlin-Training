package basic

fun main() {
    // ---> Single Line Comment
    /**
     * ---> Multi Line Comment
     */
    // The difference between var and val is that variables declared with the var keyword can be changed/modified, while val variables cannot.
    var name: String = "Dhruv"
    val age: Int = 21 // Constant Value - final keyword in Java

    println("Name: $name")
    println("Age: $age")
}